            gsap.from("#event_main_sec_6 .qu_text_box", {
                scrollTrigger: {
                    trigger: "#event_main_sec_6",
                    start: "top 70%",
                },
                opacity: 0,
                scale: 0.7,
                duration: 1.2,
                ease: "elastic.out(1, 0.5)"  // 팍! 튕기는 효과
            });

            // ========== CTA 버튼: 3D 틸트 + 그라디언트 글로우 + 로고 펄스 ==========
            $('#event_main_sec_2 .btn_box a').each(function() {
                const btn = $(this);
                let isHovering = false;
                let pulseAnim = null;
                
                // 버튼 타입별 글로우 색상
                const isType1 = btn.hasClass('btn_type1');
                const glowColor1 = isType1 ? 'rgba(0, 201, 215, 0.6)' : 'rgba(0, 201, 215, 0.5)';
                const glowColor2 = isType1 ? 'rgba(0, 201, 215, 0.4)' : 'rgba(0, 201, 215, 0.4)';
                
                // 마우스 진입 시: 스케일업 + 글로우 + 로고 표시
                btn.on('mouseenter', function(e) {
                    isHovering = true;
                    
                    // 버튼 스케일업 + 그라디언트 글로우
                    gsap.to(btn, {
                        scale: 1.05,
                        boxShadow: `0 0 20px ${glowColor1}, 0 0 40px ${glowColor2}, 0 5px 15px rgba(0,0,0,0.3)`,
                        duration: 0.4,
                        ease: "power2.out"
                    });
                    
                    // 로고는 CSS :hover로 자동 표시됨
                    
                    // 로고 펄스 애니메이션 시작 (CSS ::before의 transform 제어는 안됨)
                    // 대신 boxShadow로 펄스 효과
                    pulseAnim = gsap.to(btn, {
                        boxShadow: `0 0 30px ${glowColor1}, 0 0 60px ${glowColor2}, 0 5px 20px rgba(0,0,0,0.4)`,
                        duration: 0.8,
                        repeat: -1,
                        yoyo: true,
                        ease: "sine.inOut"
                    });
                });
                
                // 마우스 움직임: 3D 틸트 (제자리에서 회전만)
                btn.on('mousemove', function(e) {
                    if (!isHovering) return;
                    
                    const rect = btn[0].getBoundingClientRect();
                    const x = e.clientX - rect.left;
                    const y = e.clientY - rect.top;
                    
                    const centerX = rect.width / 2;
                    const centerY = rect.height / 2;
                    
                    // 3D 회전 각도 계산
                    const rotateX = (y - centerY) / 8;
                    const rotateY = (centerX - x) / 8;
                    
                    gsap.to(btn, {
                        rotationX: rotateX,
                        rotationY: rotateY,
                        transformPerspective: 1000,
                        duration: 0.4,
                        ease: "power2.out"
                    });
                });
                
                // 마우스 이탈 시: 원래 상태로 복귀
                btn.on('mouseleave', function() {
                    isHovering = false;
                    
                    // 펄스 애니메이션 중지
                    if (pulseAnim) {
                        pulseAnim.kill();
                    }
                    
                    // 버튼 복귀
                    gsap.to(btn, {
                        rotationX: 0,
                        rotationY: 0,
                        scale: 1,
                        boxShadow: '0 0 0px rgba(0,0,0,0)',
                        duration: 0.6,
                        ease: "power2.out"
                    });
                    
                    // 로고는 CSS :hover로 자동 숨김
                });
            });

        });
</script>
